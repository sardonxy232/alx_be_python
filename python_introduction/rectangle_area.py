# Rectangle Area Calculator
# Objective: Calculate the area of a rectangle using its length and width.

# Task Description:
# Your task is to write a Python script that calculates the area of a rectangle. The area of a rectangle is calculated by multiplying its length by its width.
# Instructions:
# Create a file named rectangle_area.py.
# Define two variables in this file:
length = 10  # Length of the rectangle
width = 5   # Width of the rectangle
# Calculate the area of the rectangle
area = length * width
# Print the calculated area
print(f"The area of the rectangle is: {area}")
# The code above defines two variables, length and width, with the values 10 and 5, respectively.
# It then calculates the area of the rectangle using the formula Area = length × width and prints the result in a formatted string.